class Manager {
  final String uid;
  final String name;
  final String email;
  final String phone;
  final String departmentName;
  final String managerNumber;
  final String role;
  final String status;

  Manager({
    required this.uid,
    required this.name,
    required this.email,
    required this.phone,
    required this.departmentName,
    required this.managerNumber,
    required this.role,
    required this.status,
  });
}
